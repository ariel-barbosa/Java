    private void RealizarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RealizarCadastroActionPerformed
       // criando objeto do tipo Usuario
     Usuario usuario = new Usuario();
     
     usuario.setNome(nome.getText());
     usuario.setCpf(cpf.getText());
     usuario.setEmail(email.getText());
     usuario.setTelefone(telefone.getText());
     
     if ((nome.getText().isEmpty()) || (cpf.getText().isEmpty()) || (email.getText().isEmpty()) || (telefone.getText().isEmpty())) {
        JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
            
    } else {
         // adicionando dados do tipo array
         String data[] = {nome.getText(), cpf.getText(), email.getText(), telefone.getText()};
         
         DefaultTableModel tabela = (DefaultTableModel)jTableCadastros.getModel();
         // adiciona string array data
         tabela.addRow(data); // linha adicionada
         
         // mensagem de linha inserida com sucesso
         JOptionPane.showMessageDialog(null, "Dados inseridos com Sucesso");
         
         
         
        UsuarioDAO dao = new UsuarioDAO();
        dao.adicionar(usuario);

        nome.setText("");
        cpf.setText("");
        email.setText("");
        telefone.setText("");  
     }
    }//GEN-LAST:event_RealizarCadastroActionPerformed

